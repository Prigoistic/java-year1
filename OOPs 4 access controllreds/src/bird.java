public class bird {
    class birdprops{
        String species;
        int living;
        boolean isFlying;
        String commonName;
        String color;


        public birdprops(String species, int living, boolean isFlying, String commonName, String color) {
            this.species = species;
            this.living = living;
            this.isFlying = isFlying;
            this.commonName = commonName;
            this.color = color;
        }
    }
}
